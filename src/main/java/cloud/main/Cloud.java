package cloud.main;

/**
 * The chatbot's main class.
 */
public class Cloud {
    public static void main(String[] args) {
        Cloud.line();
        System.out.println("Cloud, online.");
        Cloud.line();
        System.out.println("\\o");
        Cloud.line();
    }

    public static void line() {
        System.out.println("-".repeat(50));
    }
}
